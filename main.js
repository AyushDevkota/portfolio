function createList(id, arr) {
	const ele = document.querySelector(`#${id}`);
	let htmlList = "<ul class='list-disc pl-4'>";
	arr.forEach((item) => {
		htmlList += `<li class="pb-4">${item}</li>`;
	});
	htmlList += "</ul>";
	ele.insertAdjacentHTML("beforeend", htmlList);
}
